package model;


public class Result {

  private long tSid;
  private long tCid;
  private long tResult;


  public long getTSid() {
    return tSid;
  }

  public void setTSid(long tSid) {
    this.tSid = tSid;
  }


  public long getTCid() {
    return tCid;
  }

  public void setTCid(long tCid) {
    this.tCid = tCid;
  }


  public long getTResult() {
    return tResult;
  }

  public void setTResult(long tResult) {
    this.tResult = tResult;
  }

}
