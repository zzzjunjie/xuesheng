package model;

public class Teacher {

  private long tId;
  private String tName;
  private String tSex;
  private String tYear;
  private String tProfessional;
  private long tCid;
  private long tCnum;
  private String tPhone;
  private String tCname;


  public long getTId() {
    return tId;
  }

  public void setTId(long tId) {
    this.tId = tId;
  }


  public String getTName() {
    return tName;
  }

  public void setTName(String tName) {
    this.tName = tName;
  }


  public String getTSex() {
    return tSex;
  }

  public void setTSex(String tSex) {
    this.tSex = tSex;
  }


  public String getTYear() {
    return tYear;
  }

  public void setTYear(String tYear) {
    this.tYear = tYear;
  }


  public String getTProfessional() {
    return tProfessional;
  }

  public void setTProfessional(String tProfessional) {
    this.tProfessional = tProfessional;
  }


  public long getTCid() {
    return tCid;
  }

  public void setTCid(long tCid) {
    this.tCid = tCid;
  }


  public long getTCnum() {
    return tCnum;
  }

  public void setTCnum(long tCnum) {
    this.tCnum = tCnum;
  }


  public String getTPhone() {
    return tPhone;
  }

  public void setTPhone(String tPhone) {
    this.tPhone = tPhone;
  }


  public String getTCname() {
    return tCname;
  }

  public void setTCname(String tCname) {
    this.tCname = tCname;
  }

}
